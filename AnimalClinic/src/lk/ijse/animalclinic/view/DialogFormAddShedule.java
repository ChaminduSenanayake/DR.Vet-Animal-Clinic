    private void btnAddSheduleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddSheduleMouseClicked
        SheduleDTO sDTO = new SheduleDTO(txtSheduleID.getText(), cmbVetID.getSelectedItem().toString(), cmbDay.getSelectedItem().toString(),txtInTime.getText(),txtOutTime.getText());
        try {
            boolean result = SheduleController.addShedule(sDTO);
            if (result) {
                JOptionPane.showMessageDialog(this, "Shedule has been successfully added.");
                isAdded();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Shedule hasn't been added due to some error");
            }
        } catch (Exception ex) {
            Logger.getLogger(DialogFormAddShedule.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnAddSheduleMouseClicked

