    private void getsheduleID() {
        String newID;
        try {
            newID = IDGenarator.getNewID("shedule", "SID", "S");
            txtSheduleID.setText(newID);
        } catch (SQLException ex) {
            Logger.getLogger(DialogFormAddShedule.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DialogFormAddShedule.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadAllVetIDs() {
        try {
            ArrayList<VetDTO> allVet = VetController.getAllVets();
            cmbVetID.removeAllItems();
            if (allVet == null) {
                return;
            }
            for (VetDTO vet : allVet) {
                cmbVetID.addItem(vet.getVID());
            }
        } catch (Exception ex) {
            Logger.getLogger(DialogFormAddShedule.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }
