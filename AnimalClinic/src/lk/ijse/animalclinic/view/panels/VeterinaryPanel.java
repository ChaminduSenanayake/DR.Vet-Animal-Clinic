    private void loadAllVetIDs() {

        try {
            ArrayList<VetDTO> allVet = VetController.getAllVets();
            cmbVetID.removeAllItems();
            if (allVet == null) {
                return;
            }
            for (VetDTO vet : allVet) {
                cmbVetID.addItem(vet.getVID());
            }
        } catch (Exception ex) {
            Logger.getLogger(VeterinaryPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    private void loadAllVeterinarians() {
        try {
            ArrayList<VetDTO> allVet =VetController.getAllVets();

            DefaultTableModel dtm = (DefaultTableModel)tblVetData.getModel();
            dtm.setRowCount(0);

            if (allVet != null) {

                for (VetDTO vet : allVet) {

                    Object[] rowData = {
                        vet.getVID(),
                        vet.getfName(),
                        vet.getlName(),
                        vet.getNIC(),
                        vet.getTel(),
                        vet.getAddress(),
                        vet.getDescription()
                    };

                    dtm.addRow(rowData);

                }

            }

        } catch (Exception ex) {
            Logger.getLogger(VeterinaryPanel.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }
