    private void cmbVetIDItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbVetIDItemStateChanged
        try {
            if (cmbVetID.getSelectedIndex() == -1) return;
            VetDTO vet = VetController.searchVetByID(cmbVetID.getSelectedItem().toString());
            if (vet == null) return;
            txtFirstName.setText(vet.getfName());
            txtLastName.setText(vet.getlName());
            txtNIC.setText(vet.getNIC());
            txtAddress.setText(vet.getAddress());
            txtTelNo.setText(vet.getTel());
            txtDescription.setText(vet.getDescription());
        } catch (Exception ex) {
            Logger.getLogger(VeterinaryPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

        txtNIC.requestFocus();
    }//GEN-LAST:event_cmbVetIDItemStateChanged

    private void btnEditVetMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditVetMouseClicked
        VetDTO vet = new VetDTO(cmbVetID.getSelectedItem().toString(), txtFirstName.getText(), txtLastName.getText(), txtNIC.getText(), txtAddress.getText(), txtTelNo.getText(), txtDescription.getText());
        try {
            boolean result = VetController.editVet(vet);

            if (result) {
                JOptionPane.showMessageDialog(this, "Vet has been successfully Edited.");
                loadAllVeterinarians();
            } else {
                JOptionPane.showMessageDialog(this, "Vet hasn't been edited due to some error");
            }

        } catch (Exception ex) {
            Logger.getLogger(VeterinaryPanel.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_btnEditVetMouseClicked

    private void btnRemoveVetMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoveVetMouseClicked
        VetDTO vet = new VetDTO(cmbVetID.getSelectedItem().toString(), txtFirstName.getText(), txtLastName.getText(), txtNIC.getText(), txtAddress.getText(), txtTelNo.getText(), txtDescription.getText());
        try {
            boolean result =VetController.removeVet(vet);
            if (result) {
                JOptionPane.showMessageDialog(this, "Veterinary has been successfully Removed.");
                txtAddress.setText(null);
                txtNIC.setText(null);
                txtDescription.setText(null);
                txtFirstName.setText(null);
                txtLastName.setText(null);
                txtTelNo.setText(null);
            } else {
                JOptionPane.showMessageDialog(this, "Veterinary hasn't been Removed due to some error");
            }

        } catch (Exception ex) {
            Logger.getLogger(MedicinePanel.class.getName()).log(Level.SEVERE, null, ex);
        } 
        loadAllVeterinarians();
    }//GEN-LAST:event_btnRemoveVetMouseClicked

